/* 创建一个整型变量toes,设置为10,并计算它的2倍和平方，打印3个值 */
#include <stdio.h>
int main()
{
    int toes;
    int toes_double;
    int toes_squared;

    toes=10;
    toes_double=2*toes;
    toes_squared=toes*toes;
    printf("toes is %d, toes double is %d, toes squared is %d.\n",toes,toes_double,toes_squared);

    return 0;
}
